    @Override
    public void run() {
        //la interfaz del cliente estara constantemente refrescando la informacion
                while(true)
                {
                    try
                    {
                        //REPOSTEROS
                        //Checkeamos las galletas totales producidas por cada repostero y lo imprimimos en el JTextField
                        //Checkeamos las galletas totales desperdiciadas por cada repostero y lo imprimimos en el JTextField
                        List<Repostero> listaReposteros = metodosServer.getListaReposteros();
                        for (int indice = 0; indice < listaReposteros.size(); indice++ )
                        {
                            int galletasTotales = listaReposteros.get(indice).getTotalGalletas();
                            int galletasTotalesDesperdiciadas = listaReposteros.get(indice).getTotalGalletasDesperdiciadas();
                            getTextosReposterosGalletas(indice).setText(String.valueOf(galletasTotales));
                            getTextosReposterosGalletasDesperdiciadas(indice).setText(String.valueOf(galletasTotalesDesperdiciadas));
                        }
                        
                        //HORNOS
                        List<Horno> listaHornos = metodosServer.getListaHornos();
                        for (int indice = 0; indice < listaHornos.size(); indice++)
                        {
                            //Checkeamos el total galletas horneadas
                            int totalGalletasHorneadas = listaHornos.get(indice).getTotalGalletasHorneadas();
                            getTextosTotalGalletasHorneadas(indice).setText(String.valueOf(totalGalletasHorneadas));
                            
                            //Check si esta horneando (SI o NO)
                            if(listaHornos.get(indice).getEstaHorneando())
                            {
                                getPanelControlHorneado(indice).setBackground(Color.red);
                            }
                            else
                            {
                                getPanelControlHorneado(indice).setBackground(Color.white);
                            }
                        }
                        
                        
                        //EMPAQUETADORES
                        
                        //ALMACEN
                        
                        
                        //Finalizamos el checkeo para el cliente y esperamos un segundo hasta el siguiente checkeo
                        Thread.sleep(1000);
                    }
                    catch (InterruptedException error)
                    {
                        System.out.println("Se ha producido un error mientras la interfaz del cliente estaba actualizando los datos de la fabrica --> " + error);
                    }
                    catch (RemoteException error)
                    {
                        System.out.println("Se ha producido un error mientras se usaba algun hilo (Reposteros,Hornos,Empaquetadores) del servidor --> " + error);
                    }
                }
    }
